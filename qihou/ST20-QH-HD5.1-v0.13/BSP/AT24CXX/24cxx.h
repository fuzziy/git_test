#ifndef __24CXX_H
#define __24CXX_H
#include "stm32f10x.h"


#define AT24C01		127
#define AT24C02		255
#define AT24C04		511
#define AT24C08		1023
#define AT24C16		2047
#define AT24C32		4095
#define AT24C64	        8191
#define AT24C128	16383
#define AT24C256	32767  
//Mini STM32开发板使用的是24c02，所以定义EE_TYPE为AT24C02
#define EE_TYPE AT24C16



/***********AT24CXX包含的字节数宏定义**************************/

#define AT24C16_PAGESIZE        16               //24C16每页含的字节数

void AT24C16_WriteBytes(u16 write_addr, u8 *pwrite_buff, u16 writebytes);
void AT24CXX_PageWrite(u16 write_addr, u8 *buf, u8 write_len);
void AT24CXX_RandomRead(u16 read_addr, u8 *buf, u16 read_len);




u8 AT24CXX_ReadOneByte(u16 ReadAddr);
//在AT24CXX里面的指定地址开始写入长度为Len的数据
//该函数用于写入16bit或者32bit的数据.
//WriteAddr  :开始写入的地址  
//DataToWrite:数据数组首地址
//Len        :要写入数据的长度2,4
void AT24CXX_WriteLenByte(u16 WriteAddr,u32 DataToWrite,u8 Len);

//在AT24CXX里面的指定地址开始读出长度为Len的数据
//该函数用于读出16bit或者32bit的数据.
//ReadAddr   :开始读出的地址 
//返回值     :数据
//Len        :要读出数据的长度2,4
u32 AT24CXX_ReadLenByte(u16 ReadAddr,u8 Len);
//检查AT24CXX是否正常
//这里用了24XX的最后一个地址(255)来存储标志字.
//如果用其他24C系列,这个地址要修改
//返回1:检测失败
//返回0:检测成功
u8 AT24CXX_Check(void);

//在AT24CXX里面的指定地址开始读出指定个数的数据
//ReadAddr :开始读出的地址 对24c02为0~255
//pBuffer  :数据数组首地址
//NumToRead:要读出数据的个数
void AT24CXX_Read(u16 ReadAddr,u8 *pBuffer,u16 NumToRead);
//在AT24CXX里面的指定地址开始写入指定个数的数据
//WriteAddr :开始写入的地址 对24c02为0~255
//pBuffer   :数据数组首地址
//NumToWrite:要写入数据的个数
void AT24CXX_Write(u16 WriteAddr,u8 *pBuffer,u16 NumToWrite);

void q_AT24CXX_Write(u16 WriteAddr,u8 *pBuffer,u16 NumToWrite);
void q_AT24CXX_Read(u16 ReadAddr,u8 *pBuffer,u16 NumToRead);



#endif